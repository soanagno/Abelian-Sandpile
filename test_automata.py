import os
import numpy as np
import automata
import matplotlib.pyplot as plt

BASE_PATH = os.path.dirname(__file__)


def test_sandpile():

    initial64 = np.load(os.sep.join((BASE_PATH, 'pile_64x64_init.npy')))
    final64 = np.load(os.sep.join((BASE_PATH, 'pile_64x64_final.npy')))

    assert (automata.sandpile(initial64) == final64).all()

    initial128 = np.load(os.sep.join((BASE_PATH, 'pile_128x128_init.npy')))
    final128 = np.load(os.sep.join((BASE_PATH, 'pile_128x128_final.npy')))

    assert (automata.sandpile(initial128) == final128).all()


def test_life():

    # pdf 5x5 glider case with periodic boundaries
    # expected to be in the same place after 20 iterations
    X = 0*np.arange(5*5).reshape(5, 5)
    X[1, 1:4] = 1
    X[2, 3] = 1
    X[3, 2] = 1

    assert (automata.life(X, 20, True) == X).all()


def test_lifetri():

    # pdf 10x10 triangular glider case with periodic boundaries
    # expected to be in the same place after 30 iterations
    X = 0*np.arange(10*10).reshape(10, 10)
    X[8:10, 5:9] = 1

    assert (automata.lifetri(X, 30, True) == X).all()


